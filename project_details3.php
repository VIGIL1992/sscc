                                    <h4>Initial Costs</h4>
                                    <p>The initial costs include the purchasing price and installation or construction. These are the upfront costs associated with the building material or the equipment you choose. They’re also the costs that tend to have the most influence on purchasing decisions because they have the biggest impact on the project’s budget at the time of the decision.

                                        However, initial costs are just the tip of the iceberg. Many additional costs lie below the surface and need to be considered when evaluating the true cost and value of an item.

                                        That said, overextending your construction project’s budget for modest savings in the long term isn’t be in your best interest either. Your project team should work hard to understand your budget and present you with the options and explanations that clearly outline the full life-cycle cost of the item. That way you can make an informed decision that’s best for your project.</p>
