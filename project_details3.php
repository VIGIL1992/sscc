                        <div class="col-lg-12">
                            <p><span class="drop-cap"><span class="drop-cap-letter">W</span></span>When starting a new construction project,
                                every building owner’s goal is to maximize the value of their facility.
                                During the value engineering process, an owner and their project team look for ways to do just that. Sometimes,
                                value engineering focuses too much on reducing the short-term costs of the project, and not enough on the impact of the long-term costs.
                                While cutting short-term costs makes the building less expensive to build, it could end up costing more throughout the lifespan of the building.
                                If maximizing the value of your building is your goal, you’ll have to account for what each item will cost from the day it’s purchased to the day you dispose of it.
                                That’s where life-cycle cost analysis comes in.</p>
                            <div class="space-20"></div>
